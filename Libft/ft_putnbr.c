#include "libft.h"

void	ft_putnbr(int n)
{
	char print;
	long p;

	p = n;
	if (p < 0)
	{
		p = p * -1;
		write(1, "-", 1);
	}
	if (p >= 10)
	{
		ft_putnbr(p / 10);
		print = p % 10 + '0';
		write(1, &print, 1);
	}
	if (p < 10)
	{
		print = p + '0';
		write(1, &print, 1);
	}
}
